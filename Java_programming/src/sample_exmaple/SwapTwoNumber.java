package sample_exmaple;

public class SwapTwoNumber {

	public static void main(String[] args)
	{
	int x=194;
	int y=153;
	System.out.println("Before Swapning the Two Number: "+x +" is X value "+ y +" is the Y Value");
	
	x=x+y;
	y=x-y;
	x=x-y;
	
	System.out.println("After Swapning the Two Number: "+x +" is X value "+ y +" is the Y Value");

	}

}
